package org.example;

public class CondicionalsIV {

    public static void main(String[] args) {
        //Declaració de variables
        int a=45,b;

        //Donem valor a les vars no importa com...
        if(a>0) b=12;
        else b=24;

        //Operació equivalent utilitzant l'operador condicional ternari
        b=( a>0 ? 12 : 24 );

    }
}
